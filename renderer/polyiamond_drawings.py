def decimal_to_hex_string(dec):
  return hex(dec).split('x')[-1].zfill(2)

def randomColor():
  red = random.randint(0,255)
  green = random.randint(0,255)
  blue = random.randint(0,255)
  return "#" + decimal_to_hex_string(red) + decimal_to_hex_string(green) + decimal_to_hex_string(blue)

def render(shape, my_turtle, start, flag, screen, init):
  my_turtle.pu()
  my_turtle.goto(start)
  my_turtle.pd()

  if init == 1:
    for x in range(0,3):
      my_turtle.forward(11)
      my_turtle.left(120)

  for angle in shape:
    my_turtle.forward(11)
    my_turtle.left(angle * 60.0)

def runThisPuppy(myCollection):
  home = [300, 250]
  window = turtle.Screen()
  window.bgcolor(randomColor())
  winka = turtle.Turtle()
  winka.speed(100)

  j = 250
  k = 200
  index = 1
  for shape in myCollection:
    render(shape, winka, [j,k], index, window, 1)
    j -= 60
    if index % 11 == 0:
      j = 250
      k -= 55
    index += 1
    window.bgcolor(randomColor())

  winka.pu()
  winka.speed(1)
  winka.goto(home)
  window.exitonclick()


